# A nested list
nested_list = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Accessing elements
print("Nested List:", nested_list)
print("First sublist:", nested_list[0])        # Access the first sublist
print("Element at [1][2]:", nested_list[1][2]) # Access the element in the second sublist, third element (6)
print("Last element:", nested_list[-1][-1])    # Access the last element of the last sublist

